export const filterData = (data, items = 6) => {
    const today = data?.today?.slice() || [];
    const yesterday = data?.yesterday || [];

    if (today.length > 0) {
        const dataSet1 = today
            .sort((a, b) => a.value < b.value)
            .slice(0, items);

        const dataSet2 = [];
        dataSet1.forEach((element) => {
            const found = yesterday.find((el) => el.id === element.id);
            dataSet2.push(found);
        });

        return { today: dataSet1, yesterday: dataSet2 };
    }
};

